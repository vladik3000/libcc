#include <stdlib.h>

char	*ft_strchr(const char *s, int c)
{
	while (*s)
	{
		if (*s++ == (char)c)
			return ((char*)(s - 1));
	}
	if (*s == (char)c)
		return ((char*)s);
	else
		return (NULL);
}
